// To run this application, you must set the `VITE_API_KEY` environment variable.
// The `VITE_` prefix is MANDATORY for security reasons on platforms like Vercel
// to expose the variable to the code running in the browser.
//
// How you set this variable depends on your environment.
//
// --- Local Development ---
//
// 1. Create a file named `.env.local` in the root of your project.
// 2. Add the following line to that file:
//
//    VITE_API_KEY="YOUR_GEMINI_API_KEY_HERE"
//
//
// --- Deployment (on Vercel) ---
//
// 1. Go to your project's settings page on Vercel.
// 2. Navigate to the "Environment Variables" section.
// 3. Add a new variable with:
//    - Name: VITE_API_KEY
//    - Value: YOUR_GEMINI_API_KEY_HERE
// 4. Redeploy your application for the change to take effect.
//
// IMPORTANT: Do NOT commit your API key or the `.env.local` file to your Git repository.