// --- Local Development ---
//
// On Windows (PowerShell):
// $env:VITE_API_KEY="YOUR_GEMINI_API_KEY_HERE"
//
// On macOS/Linux:
// export VITE_API_KEY="YOUR_GEMINI_API_KEY_HERE"
//
// After setting the variable, you can start your development server in the same terminal session.
//
// --- Deployment (Vercel) ---
// Go to your project settings in Vercel, navigate to "Environment Variables",
// and add a variable with the name `VITE_API_KEY` and your key as the value.
