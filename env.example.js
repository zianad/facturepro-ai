// This application uses the Google Gemini API for its intelligent features.
// To function correctly, it requires an API key.

//
// IMPORTANT: Do NOT paste your API key directly into this file or any other
// source code file.
//

// The API key must be provided as an environment variable named `API_KEY`
// in the environment where you build or host the application. The application
// code is already set up to read this variable.

// --- For Local Development ---
// If you are using a modern development server (like Vite or Create React App),
// you can create a file named `.env` in the project root and add your key:
//
// API_KEY="YOUR_GEMINI_API_KEY_HERE"
//
// The development server will automatically make this available to the app.

// If you are running a simple static server, you will need to set this
// environment variable in your terminal before starting the server.
//
// Example (macOS/Linux):
// export API_KEY="YOUR_GEMINI_API_KEY_HERE"
// npm run dev
//
// Example (Windows PowerShell):
// $env:API_KEY="YOUR_GEMINI_API_KEY_HERE"
// npm run dev
